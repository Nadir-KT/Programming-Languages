/******************************************************************************
2D Array: 2rows & 3columns 
*******************************************************************************/
#include <stdio.h>
int arr[2][3] = {{2,2,2},
                 {3,3,3}};
int main()
{
    for(int i=0;i<2;i++) {
        for(int j=0;j<3;j++) {
            printf("%d\t",arr[i][j]);
        }
        printf("\n");
    }
    return 0;
}