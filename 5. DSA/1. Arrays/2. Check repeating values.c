/******************************************************************************
I/p - 10, 11, 12, 13, 13, 13, 12, 14
 
Expected O/p 
10 -> 1
11 -> 1
12-> 2
13 -> 3
14 -> 1
*******************************************************************************/
#include <stdio.h>
int arr[8] = {10, 11, 12, 13, 13, 13, 12, 14};
int arrOut[8] = {0};
int i=0,j=0;
int main()
{
    for(i; i<8; i++) {
	for(j=0; j<8; j++){
		if (arr[i]==arr[j]){
			arrOut[i]=arrOut[i]+1;
		}
	}
	printf("%d -> %d \n",arr[i], arrOut[i]); 
    }
    return 0;
}